using System.IO;
using UnityEditor;
using UnityEngine;
using UnityEngine.UI;
using Elementborn.Game;

namespace Elementborn.Game.EditorTools
{
    public static class NpcAdminEditorTools
    {
        private const string PrefabDir = "Assets/Elementborn/Generated/Prefabs/NPC";
        private const string ReportDir = "Assets/Elementborn/Generated/Reports";
        private const string ManifestPath = "Assets/Elementborn/Generated/NPC/Integration/NpcWorldIntegrationManifest.asset";

        [MenuItem("Elementborn/NPC Tools/Create NPC Admin UI Prefab")]
        public static void CreateNpcAdminUiPrefab()
        {
            Directory.CreateDirectory(PrefabDir);
            GameObject canvas = new GameObject("Elementborn_NpcAdminCanvas");
            var c = canvas.AddComponent<Canvas>();
            c.renderMode = RenderMode.ScreenSpaceOverlay;
            canvas.AddComponent<CanvasScaler>();
            canvas.AddComponent<GraphicRaycaster>();

            GameObject panel = CreatePanel(canvas.transform, "NPC Admin Panel", new Vector2(0, 0), new Vector2(760, 560));
            var view = panel.AddComponent<NpcAdminPanelView>();

            Text title = CreateText(panel.transform, "Title", "NPC Admin", 30, new Vector2(0, -28), new Vector2(700, 40));
            InputField search = CreateInput(panel.transform, "Search Input", "Search name, origin, notes...", new Vector2(0, -85), new Vector2(660, 38));
            InputField region = CreateInput(panel.transform, "Region Input", "Region filter...", new Vector2(-170, -135), new Vector2(320, 34));
            InputField element = CreateInput(panel.transform, "Element Input", "Element filter...", new Vector2(170, -135), new Vector2(320, 34));
            Text results = CreateText(panel.transform, "Results Text", "Run refresh to list NPCs.", 16, new Vector2(0, -330), new Vector2(680, 330));
            results.alignment = TextAnchor.UpperLeft;

            SetPrivate(view, "searchInput", search);
            SetPrivate(view, "regionInput", region);
            SetPrivate(view, "elementInput", element);
            SetPrivate(view, "resultsText", results);

            PrefabUtility.SaveAsPrefabAsset(canvas, $"{PrefabDir}/Elementborn_NpcAdminCanvas.prefab");
            Object.DestroyImmediate(canvas);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();
            Debug.Log("Created NPC Admin UI prefab.");
        }

        [MenuItem("Elementborn/NPC Tools/Install NPC Admin Tools In Open Scene")]
        public static void InstallNpcAdminTools()
        {
            NpcWorldIntegrationAssetGenerator.GenerateAll();
            CreateNpcAdminUiPrefab();

            GameObject tools = GameObject.Find("Elementborn NPC Admin Tools");
            if (tools == null)
            {
                tools = new GameObject("Elementborn NPC Admin Tools");
            }

            var registry = tools.GetComponent<NpcAdminRegistry>();
            if (registry == null)
            {
                registry = tools.AddComponent<NpcAdminRegistry>();
            }

            var manifest = AssetDatabase.LoadAssetAtPath<NpcWorldIntegrationManifest>(ManifestPath);
            registry.SetManifest(manifest);

            if (tools.GetComponent<NpcAdminCommandBridge>() == null)
            {
                tools.AddComponent<NpcAdminCommandBridge>();
            }

            if (tools.GetComponent<NpcPlacementAdminTool>() == null)
            {
                tools.AddComponent<NpcPlacementAdminTool>();
            }

            GameObject prefab = AssetDatabase.LoadAssetAtPath<GameObject>($"{PrefabDir}/Elementborn_NpcAdminCanvas.prefab");
            if (prefab != null && GameObject.Find("Elementborn_NpcAdminCanvas") == null)
            {
                PrefabUtility.InstantiatePrefab(prefab);
            }

            EditorUtility.SetDirty(tools);
            Debug.Log("Installed NPC Admin Tools in open scene.");
        }

