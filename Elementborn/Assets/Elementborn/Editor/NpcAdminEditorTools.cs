        private static GameObject CreatePanel(Transform parent, string name, Vector2 position, Vector2 size)
        {
            GameObject go = new GameObject(name);
            go.transform.SetParent(parent, false);
            RectTransform rect = go.AddComponent<RectTransform>();
            rect.anchorMin = rect.anchorMax = new Vector2(0.5f, 0.5f);
            rect.pivot = new Vector2(0.5f, 0.5f);
            rect.anchoredPosition = position;
            rect.sizeDelta = size;
            Image image = go.AddComponent<Image>();
            image.color = new Color(0.06f, 0.16f, 0.26f, 0.94f);
            return go;
        }

        private static Text CreateText(Transform parent, string name, string text, int size, Vector2 position, Vector2 rectSize)
        {
            GameObject go = new GameObject(name);
            go.transform.SetParent(parent, false);
            RectTransform rect = go.AddComponent<RectTransform>();
            rect.anchorMin = rect.anchorMax = new Vector2(0.5f, 0.5f);
            rect.pivot = new Vector2(0.5f, 0.5f);
            rect.anchoredPosition = position;
            rect.sizeDelta = rectSize;
            Text label = go.AddComponent<Text>();
            label.text = text;
            Elementborn.Game.ElementbornBuiltinFontUtility.ApplyDefaultFont(label);
            label.fontSize = size;
            label.alignment = TextAnchor.MiddleCenter;
            label.color = new Color(1f, 0.96f, 0.86f, 1f);
            label.horizontalOverflow = HorizontalWrapMode.Wrap;
            label.verticalOverflow = VerticalWrapMode.Overflow;
            return label;
        }

        private static InputField CreateInput(Transform parent, string name, string placeholder, Vector2 position, Vector2 rectSize)
        {
            GameObject root = new GameObject(name);
            root.transform.SetParent(parent, false);
            RectTransform rect = root.AddComponent<RectTransform>();
            rect.anchorMin = rect.anchorMax = new Vector2(0.5f, 0.5f);
            rect.pivot = new Vector2(0.5f, 0.5f);
            rect.anchoredPosition = position;
            rect.sizeDelta = rectSize;
            Image image = root.AddComponent<Image>();
            image.color = new Color(1f, 1f, 1f, 0.18f);
            InputField input = root.AddComponent<InputField>();

            Text text = CreateText(root.transform, "Text", "", 16, Vector2.zero, rectSize - new Vector2(16, 8));
            text.alignment = TextAnchor.MiddleLeft;
            Text placeholderText = CreateText(root.transform, "Placeholder", placeholder, 16, Vector2.zero, rectSize - new Vector2(16, 8));
            placeholderText.alignment = TextAnchor.MiddleLeft;
            placeholderText.color = new Color(1f, 1f, 1f, 0.45f);

            input.textComponent = text;
            input.placeholder = placeholderText;
            return input;
        }

        private static void SetPrivate(object target, string fieldName, object value)
        {
            var field = target.GetType().GetField(fieldName, System.Reflection.BindingFlags.Instance | System.Reflection.BindingFlags.NonPublic);
            if (field != null)
            {
                field.SetValue(target, value);
                EditorUtility.SetDirty(target as Object);
            }
        }
    }
